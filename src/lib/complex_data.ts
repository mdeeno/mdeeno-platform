export type ComplexStage = 'planning' | 'approval' | 'management' | 'construction';

export interface ComplexData {
  complex_name: string;
  location: string;
  avg_asset_value?: number;
  stage?: ComplexStage;
  expected_cost?: number;
}

export const STAGE_LABELS: Record<ComplexStage, string> = {
  planning:     '기본계획 수립',
  approval:     '사업시행인가',
  management:   '관리처분인가',
  construction: '착공 / 공사 중',
};

export function findComplex(name: string): ComplexData | undefined {
  const decoded = decodeURIComponent(name);
  const entry = COMPLEX_DATASET.find(
    (c) => c.complex_name === name || c.complex_name === decoded,
  );
  if (!entry) return undefined;
  return {
    complex_name: entry.complex_name,
    location: entry.city + ' ' + entry.district,
  };
}

export function getComplexesByLocation(city: string): ComplexEntry[] {
  return COMPLEX_DATASET.filter(
    (c) => c.city === city || c.district.includes(city),
  );
}
